package com.tj.op;

public class Ex06 {
	public static void main(String[] args) {
		int n1 = 10*3; // n1 은 30
		n1 += 10; // n1 = n1+10 (n1이 40)
		n1 -= 10; // n1 = n1-10 (n1이 30)
		n1 *= 10; // n1 = n1*10 (n1이 300)
		n1 /= 10; // n1 = n1/10 (n1이 30)
		n1 %= 10; // n1 = n1&10 (n1이 0)
		
		System.out.println(n1);
		
		
	}

}
