DROP TABLE MEMBER CASCADE CONSTRAINTS;
DROP TABLE BOOK;

CREATE TABLE BOOK(
    bNUM NUMBER(8) PRIMARY KEY,
    bTITLE VARCHAR2(100) NOT NULL,
    bWRITER VARCHAR2(100) NOT NULL,
    bRDATE DATE DEFAULT SYSDATE,
    bIMG1 VARCHAR2(100) DEFAULT 'noImg.png',
    bIMG2 VARCHAR2(100) DEFAULT 'noImg.png',
    bINFO VARCHAR2(1000)
);

CREATE TABLE MEMBER(
    mID VARCHAR2(100) PRIMARY KEY,
    mPW VARCHAR2(100) NOT NULL,
    mNAME VARCHAR2(100) NOT NULL,
    mMAIL VARCHAR2(100) NOT NULL,
    mPOST VARCHAR2(100), -- 우편번호
    mADDR VARCHAR2(100) -- 주소
);


SELECT * FROM MEMBER;